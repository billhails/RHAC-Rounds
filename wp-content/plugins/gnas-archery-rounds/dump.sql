CREATE VIEW max_scores AS
    SELECT name, sum(num_arrows) * max_score AS max
    FROM round_family, arrow_count, scorings
    WHERE round_family.name = arrow_count.family_name
    AND round_family.scoring = scorings.scoring
    GROUP BY name;
CREATE VIEW max_scores_per_round AS
    SELECT round.name AS round, max
    FROM round, max_scores
    WHERE round.family_name = max_scores.name;
